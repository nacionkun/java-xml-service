package org.example;

public class Person {

    private String firstname;
    private String lastname;
    private PhoneNumber phone;
    private PhoneNumber fax;

    public String getFirstname() {
        return firstname;
    }
    public String getLastname() {
        return lastname;
    }

    public PhoneNumber getPhone() {
        return phone;
    }

    public PhoneNumber getFax() {
        return fax;
    }
}
